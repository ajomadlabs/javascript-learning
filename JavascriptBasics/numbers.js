/* Numbers */

/*
With JavaScript numbers we can do all the matheatical operations ranging from 
1. Addition
2. Subtraction
3. Multiplication
4. Division
5. Modulos
6. Increment 
7. Decrement
*/

var myNumberOne = 12;
var myNumberTwo = 13
console.log(myNumberOne + myNumberTwo);
console.log(myNumberOne - myNumberTwo);
console.log(myNumberOne * myNumberTwo);
console.log(myNumberOne / myNumberTwo);
console.log(myNumberOne % myNumberTwo);
console.log(myNumberOne++);
console.log(myNumberOne);
console.log(++myNumberOne);